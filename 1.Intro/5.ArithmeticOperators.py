a = 17
b = 2

print("Sum \t\t {0} + {1} = {2}".format(a, b, a + b))
print("Difference \t {0} - {1} = {2}".format(a, b, a - b))
print("Product \t {0} * {1} = {2}".format(a, b, a * b))
print("Float Division \t {0} / {1} = {2}".format(a, b, a / b))
print("Integer Division {0} // {1} = {2}".format(a, b, a // b))
print("Modulo Division  {0} % {1} = {2}".format(a, b, a % b))
print("Power \t\t {0} ** {1} = {2}".format(a, b, a ** b))
