import math

# Absolute value - How far from 0
print("abs(3.2) = {0}".format(math.fabs(3.2)))
print("abs(-12.89) = {0}".format(math.fabs(-12.89)))

# Ceiling - Smallest Integer not less than the value
print("ceil(3.2) = {0}".format(math.ceil(3.2)))

# Square Root
print("Square Root of 15 is {0:10.6}".format(math.sqrt(15)))

# Inbuilt factoril, GCD
print("Factorial of 5 is {0}".format(math.factorial(5)))
print("GCD of 12, 18 is {0}".format(math.gcd(12, 18)))

# Contants - pi, e
print("Pi - {0}".format(math.pi))
print("e - {0}".format(math.e))

